"""
+	Addition
-	Subtraction
*	Multiplication
/	Division
%	Modulus
**	Exponentiation
//	Floor division
"""

x = 10
x += 3

y = 10
y *= 10

print (x)
print (y)

#Operator Precedence
a = 10 + 5 * 2
#expected output = 20
b = (10 + 5) * 2
#expected output = 30
print (a)
print (b)